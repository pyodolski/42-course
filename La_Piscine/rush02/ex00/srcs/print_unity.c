#include "rush.h"

void	print_unity(char *nb, char **dico, int *i)
{
	int pos;

	if (nb[0] != '0')
	{
		if (*i)
			write(1, " ", 1);
		*i = *i + 1;
		pos = nb[0] - '0';
		write(1, dico[pos], ft_strlen(dico[pos]));
	}
}
